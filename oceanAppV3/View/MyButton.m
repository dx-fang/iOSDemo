#import "MyButton.h"

@implementation MyButton
/*
 UIButtonType是一个枚举类型，用于定义UIButton的样式和行为。以下是一些常用的UIButtonType类型及其区别：
 UIButtonTypeCustom:自定义按钮。不提供任何按钮样式，默认情况下不呈现任何视觉样式或装饰，完全依赖于你自己的设计。
 UIButtonTypeSystem (以前称为UIButtonTypeRoundedRect): 系统按钮，iOS 7及以后的默认样式。
 这种类型的按钮会根据操作系统版本自动调整其外观，以符合用户的期望。它会呈现一个标准的系统外观，包括在用户交互时的视觉
 UIButtonTypeDetailDisclosure:详情披露按钮，通常用于表视图单元格中，可以披露更多信息。样式是一个圆形的信息按钮
 UIButtonTypeInfoLight 和 UIButtonTypeInfoDark:信息按钮，分别用于浅色和深色背景。
 这些按钮通常用于提供关于某个控件或操作的额外信息。
 UIButtonTypeContactAdd: 添加联系人按钮，通常用于添加项目或联系人。它的样式是一个带有加号的圆形按钮。
 
 每种按钮类型都有其特定的用途和默认外观，但是你可以通过设置按钮的属性（如标题、图像等）来自定义这些外观。
 UIButtonTypeCustom提供了最大的灵活性，因为它允许你从头开始定义按钮的外观，而其他类型则提供了一些默认的视觉样式，这些样式在不同版本的iOS中可能会有所不同，以保持与操作系统的一致性。
 */

@end
